// 오늘의 문장과 모드를 결정하는 함수 (날짜 기반)
export const getTodaysChallenge = () => {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));

  // 하루에 하나의 문제와 하나의 모드를 번갈아 제공
  const quoteIndex = dayOfYear % quotes.length;
  const modeIndex = dayOfYear % 3; // 0: word, 1: consonant, 2: shuffle

  const selectedQuote = quotes[quoteIndex];
  let gameMode = 'word';
  let modeDisplayName = '단어 찾기';

  if (modeIndex === 1) {
    gameMode = 'consonant';
    modeDisplayName = '초성 퀴즈';
  } else if (modeIndex === 2) {
    gameMode = 'shuffle';
    modeDisplayName = '글자 순서 배열';
  }

  return {
    sentence: selectedQuote.text,
    source: selectedQuote.source,
    type: selectedQuote.type,
    mode: gameMode,
    modeDisplayName: modeDisplayName, // 한글 모드 이름 추가
  };
}; 